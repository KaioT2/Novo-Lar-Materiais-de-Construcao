    private void jButton1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseReleased
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(this);
        String date = new SimpleDateFormat("yyyy-MM-dd").format(new Date());

        try {
            File f = fc.getSelectedFile();
            pasta = f.getAbsolutePath();
            pasta = pasta.replace('\\', '/');
            pasta = pasta + "_" + date + ".sql";
            txtLocalDoArquivo.setText(pasta);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1MouseReleased

    private void jButton2MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseReleased
        // TODO add your handling code here:
        if (txtLocalDoArquivo.getText().equals("")) {

            txtMensagem.setText("*ESCOLHA O LOCAL DO ARQUIVO*");
        } else {
            Process p = null;
            try {
                Runtime runtime = Runtime.getRuntime();
                p = runtime.exec("C:/xampp/mysql/bin/mysqldump.exe -uroot --add-drop-database -B dbnovolar -r" + pasta);

                int processComplete = p.waitFor();
                if (processComplete == 0) {
                    txtMensagem.setText("Backup criado com sucesso");
                } else {
                    txtMensagem.setText("Não foi possível criar o backup");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2MouseReleased


