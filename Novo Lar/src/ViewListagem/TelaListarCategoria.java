    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tabelaCategoria.getSelectedRow() != -1) {
            Categoria c = new Categoria();
            CategoriaDAO dao = new CategoriaDAO();

            c.setIdCategoria((int) tabelaCategoria.getValueAt(tabelaCategoria.getSelectedRow(), 0));

            dao.delete(c);
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Categoria para excluir");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:

        if (tabelaCategoria.getSelectedRow() != -1) {
            Categoria c = new Categoria();
            CategoriaDAO dao = new CategoriaDAO();

            c.setIdCategoria(Integer.parseInt(tabelaCategoria.getValueAt(tabelaCategoria.getSelectedRow(), 0).toString()));
            c.setDescricao(tabelaCategoria.getValueAt(tabelaCategoria.getSelectedRow(), 1).toString());
            
            dao.update(c);
            atualizarTabela();
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

