       public void atualizarTabela() {
        DefaultTableModel modelo = (DefaultTableModel)  tabelaCategoria.getModel();
        modelo.setNumRows(0);
           CategoriaDAO cdao = new CategoriaDAO();

        for (Categoria c : cdao.read()) {

            modelo.addRow(new Object[]{
               c.getIdCategoria(),
               c.getDescricao()
            });

        }

    }

    public void searchJTableForName(String descricao) {
        DefaultTableModel modelo = (DefaultTableModel) tabelaCategoria.getModel();
        modelo.setNumRows(0);
        CategoriaDAO cdao = new CategoriaDAO();

        for (Categoria c : cdao.searchForName(descricao)) {

            modelo.addRow(new Object[]{
                c.getIdCategoria(),
                c.getDescricao()
            });

        }

    }

